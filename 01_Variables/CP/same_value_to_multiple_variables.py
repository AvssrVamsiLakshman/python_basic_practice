# ============================================================
# Concept: Multiple Assignments
# Topic: Chained Assignment (Same Value to Multiple Variables)
# ============================================================
# Description: Assigning the same value to multiple variables
#              using chained assignment.
# Syntax: var1 = var2 = var3 = value
# ============================================================

# Assigning same value to multiple variables
x = y = z = 100

# All three variables have the same value
print("Value of x:", x)    # Output: Value of x: 100
print("Value of y:", y)    # Output: Value of y: 100
print("Value of z:", z)    # Output: Value of z: 100
