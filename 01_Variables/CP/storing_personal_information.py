# ============================================================
# Concept: Variables - Multiple Data Types
# Topic: Storing Personal Information
# ============================================================
# Description: Using different data types to store personal
#              information:
#              - String for name
#              - Integer for age
#              - Float for height
# ============================================================

# Storing personal information in variables
name = "Vamsi Lakshman"   # String - stores text
age = 27                   # Integer - stores whole number
height = 5.9               # Float - stores decimal number

# Displaying personal information
print(name)       # Output: Vamsi Lakshman
print(age)        # Output: 27
print(height)     # Output: 5.9

