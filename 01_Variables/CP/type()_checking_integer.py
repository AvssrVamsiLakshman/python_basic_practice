# ============================================================
# Concept: type() Function
# Topic: Checking the Type of an Integer Variable
# ============================================================
# Description: The type() function returns the data type of
#              a variable. Integer type is shown as <class 'int'>.
# Syntax: type(variable)
# ============================================================

# Creating an integer variable
age = 25

# Checking and printing the type
print(type(age))    # Output: <class 'int'>
