# ============================================================
# Concept: Arithmetic Operations
# Topic: Division of Two Numbers
# ============================================================
# Description: Using the / operator to divide two numbers.
#              Division always returns a float result.
# Syntax: result = number1 / number2
# ============================================================

# Creating two integer variables
a = 100
b = 4

# Dividing the two numbers
result = a / b

# Displaying the quotient (result is float: 25.0)
print("Quotient:", result)    # Output: Quotient: 25.0
