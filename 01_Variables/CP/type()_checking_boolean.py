# ============================================================
# Concept: type() Function
# Topic: Checking the Type of a Boolean Variable
# ============================================================
# Description: The type() function returns the data type of
#              a variable. Boolean type is shown as <class 'bool'>.
# Syntax: type(variable)
# ============================================================

# Creating a boolean variable
is_student = True

# Checking and printing the type
print(type(is_student))    # Output: <class 'bool'>
