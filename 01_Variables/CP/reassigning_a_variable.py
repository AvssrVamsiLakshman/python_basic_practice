# ============================================================
# Concept: Variables
# Topic: Reassigning a Variable
# ============================================================
# Description: In Python, you can change (reassign) the value
#              of a variable at any time. The old value is
#              replaced with the new value.
# ============================================================

# Initial assignment
score = 50
print("Initial score:", score)    # Output: Initial score: 50

# Reassigning with new value
score = 85
print("Updated score:", score)    # Output: Updated score: 85
