# ============================================================
# Concept: Data Types in Python
# Topic: All Four Basic Data Types
# ============================================================
# Description: Python has four basic data types:
#              1. String (str) - Text data in quotes
#              2. Integer (int) - Whole numbers
#              3. Float (float) - Decimal numbers
#              4. Boolean (bool) - True or False
# ============================================================

# String - stores text data
name = "Vamsi Lakshman"

# Integer - stores whole numbers
age = 21

# Float - stores decimal numbers
height = 5.9

# Boolean - stores True or False
is_student = True

# Displaying each variable with its type
print("Name:", name, "- Type:", type(name))
print("Age:", age, "- Type:", type(age))
print("Height:", height, "- Type:", type(height))
print("Is Student:", is_student, "- Type:", type(is_student))
