# ============================================================
# Concept: Data Types - Boolean
# Topic: Printing Boolean Values
# ============================================================
# Description: Boolean is a data type that can only have two
#              values: True or False. Used for conditions.
# Note: True and False are case-sensitive (capital T and F).
# ============================================================

# Creating boolean variables
is_it_sunny = True      # Boolean True
is_it_raining = False   # Boolean False

# Printing boolean values
print(is_it_sunny)      # Output: True
print(is_it_raining)    # Output: False