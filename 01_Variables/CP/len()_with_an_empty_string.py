# ============================================================
# Concept: len() Function
# Topic: Length of an Empty String
# ============================================================
# Description: The len() function returns the number of
#              characters in a string. An empty string has
#              a length of 0.
# Syntax: len(string)
# ============================================================

# Creating an empty string
empty_string = ""

# Getting the length of empty string
length = len(empty_string)

# Output: 0
print(length)