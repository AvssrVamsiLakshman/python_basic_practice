# ============================================================
# Concept: len() Function
# Topic: Length of a Sentence (String)
# ============================================================
# Description: The len() function counts ALL characters in a
#              string including spaces and punctuation marks.
# ============================================================

# Creating a sentence string
sentence = "This is a sample sentence."

# Getting the length (includes spaces and period)
length = len(sentence)

# Output: 27 (counts all characters including spaces and dot)
print(length)