# ============================================================
# Concept: type() Function
# Topic: Checking the Type of a Float Variable
# ============================================================
# Description: The type() function returns the data type of
#              a variable. Float type is shown as <class 'float'>.
# Syntax: type(variable)
# ============================================================

# Creating a float variable (decimal number)
height = 5.9

# Checking and printing the type
print(type(height))    # Output: <class 'float'>
