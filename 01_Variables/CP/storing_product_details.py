# ============================================================
# Description: Using variables to store product information
#              for an e-commerce application.
#              - String for product name
#              - Float for price (with decimals)
#              - Integer for quantity (whole number)
# ============================================================

# Storing product details
product_name = "Laptop"     # String - product name
product_price = 999.99      # Float - price with decimals
stock_quantity = 50         # Integer - quantity in stock

# Displaying product details
print(product_name)     # Output: Laptop
print(product_price)    # Output: 999.99
print(stock_quantity)   # Output: 50