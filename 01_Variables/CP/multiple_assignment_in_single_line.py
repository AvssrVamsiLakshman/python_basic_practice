# ============================================================
# Concept: Multiple Assignments
# Topic: Assigning Multiple Values in Single Line
# ============================================================
# Description: Python allows assigning multiple values to
#              multiple variables in a single line.
# Syntax: var1, var2, var3 = value1, value2, value3
# ============================================================

# Assigning multiple values to multiple variables
name, age, city = "Vamsi", 21, "Hyderabad"

# Displaying all variables
print("Name:", name)      # Output: Name: Vamsi
print("Age:", age)        # Output: Age: 21
print("City:", city)      # Output: City: Hyderabad
