# ============================================================
# Concept: String Operations
# Topic: String Concatenation
# ============================================================
# Description: String concatenation means joining two or more
#              strings together using the + operator.
# Syntax: new_string = string1 + string2
# ============================================================

# Creating string variables
greeting = "Hello"
name = "Vamsi"

# Concatenating strings (joining them together)
message = greeting + " " + name + "!"

# Displaying the concatenated string
print(message)    # Output: Hello Vamsi!
