# ============================================================
# Concept: input() Function
# Topic: Taking Multiple Inputs from User
# ============================================================
# Description: Using input() multiple times to get two numbers
#              from the user and displaying them.
# Note: input() always returns a string, even for numbers.
# ============================================================

# Taking two numbers as input
num1 = input("Please enter the first number: ")
num2 = input("Please enter the second number: ")

# Displaying both numbers
print("The first number you entered is:")
print(num1)
print("And")
print("The second number you entered is:")
print(num2)