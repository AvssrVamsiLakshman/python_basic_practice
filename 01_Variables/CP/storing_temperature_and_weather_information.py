# Topic: Storing Temperature and Weather Information
# ============================================================
# Description: Using variables to store weather data.
#              - Float for temperature (can have decimals)
#              - String for weather condition (text)
# ============================================================

# Storing weather information
temperature_celsius = 22.5      # Float - temperature in Celsius
weather_condition = "Sunny"     # String - weather description

# Displaying weather information
print(temperature_celsius)   # Output: 22.5
print(weather_condition)     # Output: Sunny