# ============================================================
# Concept: print() Function & Data Types
# Topic: Printing Numbers (Integer and Float)
# ============================================================
# Description: print() can directly print numbers.
#              - Integer: Whole numbers (e.g., 25)
#              - Float: Decimal numbers (e.g., 3.14)
# ============================================================

# Printing an integer (whole number)
print(25)       # Output: 25

# Printing a float (decimal number)
print(3.14)     # Output: 3.14