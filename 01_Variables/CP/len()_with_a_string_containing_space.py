# ============================================================
# Concept: len() Function
# Topic: Length of a String with Space
# ============================================================
# Description: Spaces are also counted as characters.
#              "Hello World" has 11 characters (including space).
# ============================================================

# Creating a string with space
word_with_space = "Hello World"

# Getting the length (space is counted as a character)
length = len(word_with_space)

# Output: 11 (H-e-l-l-o-space-W-o-r-l-d)
print(length)