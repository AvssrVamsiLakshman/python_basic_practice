# ============================================================
# Concept: Naming Conventions
# Topic: snake_case Naming Convention
# ============================================================
# Description: snake_case uses lowercase letters with words
#              separated by underscores. This is the RECOMMENDED
#              style for Python variable names (PEP 8).
# Example: first_name, last_name, total_amount
# ============================================================

# Using snake_case for variable names
first_name = "Vamsi"
last_name = "Lakshman"

# Concatenating to create full name
full_name = first_name + " " + last_name

# Displaying the result
print("Full Name:", full_name)    # Output: Full Name: Vamsi Lakshman
