# ============================================================
# Concept: Naming Conventions
# Topic: camelCase Naming Convention
# ============================================================
# Description: camelCase starts with a lowercase letter and
#              each subsequent word starts with uppercase.
#              Common in JavaScript, but valid in Python.
# Example: firstName, lastName, totalAmount
# ============================================================

# Using camelCase for variable names
firstName = "Vamsi"
lastName = "Lakshman"

# Concatenating to create full name
fullName = firstName + " " + lastName

# Displaying the result
print("Full Name:", fullName)    # Output: Full Name: Vamsi Lakshman
