# ============================================================
# Concept: Operator Precedence
# Topic: Negative Numbers
# ============================================================
# Description: Working with negative numbers in expressions.
#              Order: (4*-2)=-8, -3+(-8)=-11, -11+5=-6.
#              Parentheses are evaluated first, then left-to-right.
# Precedence: () > * > + = -
# ============================================================

# Expression with negative numbers
result = -3 + (4 * -2) + 5  # (4*-2)=-8, -3-8=-11, -11+5=-6

# Displaying the result
print(result)               # Output: -6
