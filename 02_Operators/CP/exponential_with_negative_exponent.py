# ============================================================
# Concept: Arithmetic Operators
# Topic: Exponential with Negative Exponent
# ============================================================
# Description: Using a negative exponent with the ** operator.
#              A negative exponent gives the reciprocal:
#              2^-2 = 1/(2^2) = 1/4 = 0.25
# Syntax: result = base ** negative_exponent
# ============================================================

# Defining a base number
a = 2                   # Base number

# Using the exponential operator with negative exponent
result = a ** -2        # 2^-2 = 1/(2^2) = 0.25

# Displaying the result
print(result)           # Output: 0.25
