# ============================================================
# Concept: Operator Precedence
# Topic: Exponents with Parentheses
# ============================================================
# Description: Parentheses are evaluated first, then exponent,
#              then multiplication. Order: (3+2)=5, 5^2=25, 25*2=50.
# Precedence: () > ** > *
# ============================================================

# Expression with parentheses, exponent and multiplication
result = (3 + 2) ** 2 * 2  # (3+2)=5, 5^2=25, 25*2=50

# Displaying the result
print(result)              # Output: 50
