# ============================================================
# Concept: Operator Precedence
# Topic: Multiplication Before Addition
# ============================================================
# Description: Multiplication has higher precedence than addition.
#              Order of evaluation: 2 * 3 = 6, then 7 + 6 = 13.
#              This is the standard BODMAS/PEMDAS rule.
# Precedence: * > +
# ============================================================

# Expression with multiplication and addition
result = 7 + 2 * 3      # 2*3=6, 7+6=13

# Displaying the result
print(result)           # Output: 13
