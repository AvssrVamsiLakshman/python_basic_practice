# ============================================================
# Concept: Operator Precedence
# Topic: Simple Parentheses
# ============================================================
# Description: Parentheses have the highest precedence and
#              are evaluated first. Here, (2 + 3) is calculated
#              before multiplication: 6 * 5 = 30.
# Syntax: result = value * (expression)
# ============================================================

# Using parentheses to control order of operations
result = 6 * (2 + 3)    # First: 2 + 3 = 5, Then: 6 * 5 = 30

# Displaying the result
print(result)           # Output: 30
