# ============================================================
# Concept: Comparison Operators
# Topic: Greater Than or Equal to with Zero
# ============================================================
# Description: Using the >= operator to compare with zero.
#              Returns True if the value is greater than or
#              equal to zero.
# Syntax: result = (value >= 0)
# ============================================================

# Defining a variable with zero value
a = 0                   # Zero value

# Comparing with zero using >= operator
result = (a >= 0)       # 0 >= 0 is True

# Displaying the result
print(result)           # Output: True
