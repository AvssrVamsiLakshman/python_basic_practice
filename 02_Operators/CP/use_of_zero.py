# ============================================================
# Concept: Operator Precedence
# Topic: Use of Zero
# ============================================================
# Description: Any number multiplied by zero equals zero.
#              Order: (10-10)=0, 5*0=0, 0+1=1.
#              Demonstrates the zero property of multiplication.
# Precedence: () > * > +
# ============================================================

# Expression demonstrating multiplication with zero
result = 5 * (10 - 10) + 1  # (10-10)=0, 5*0=0, 0+1=1

# Displaying the result
print(result)               # Output: 1
