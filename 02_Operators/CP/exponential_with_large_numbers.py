# ============================================================
# Concept: Arithmetic Operators
# Topic: Exponential with Large Numbers
# ============================================================
# Description: Using the ** operator with large numbers.
#              Python can handle very large numbers without
#              overflow: 10^10 = 10,000,000,000
# Syntax: result = base ** exponent
# ============================================================

# Defining base and exponent
a = 10                  # Base number
b = 10                  # Exponent

# Using the exponential operator
result = a ** b

# Displaying the result
print(result)           # Output: 10000000000
