# ============================================================
# Concept: Arithmetic Operators
# Topic: Negative Base
# ============================================================
# Description: Using a negative base with the ** operator.
#              When a negative number is raised to an even
#              power, the result is positive (-3)^2 = 9.
# Syntax: result = negative_base ** exponent
# ============================================================

# Defining a negative base and a positive exponent
a = -3                  # Negative base
b = 2                   # Positive exponent (even number)

# Using the exponential operator
result = a ** b

# Displaying the result
print(result)           # Output: 9
