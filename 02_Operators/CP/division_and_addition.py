# ============================================================
# Concept: Operator Precedence
# Topic: Division and Addition
# ============================================================
# Description: Division has higher precedence than addition.
#              Order of evaluation: 10/2 = 5.0, then 5.0 + 5 = 10.0
#              Note: Division always returns a float in Python.
# Precedence: / > +
# ============================================================

# Expression with division and addition
result = 10 / 2 + 5     # 10/2=5.0, 5.0+5=10.0

# Displaying the result
print(result)           # Output: 10.0
