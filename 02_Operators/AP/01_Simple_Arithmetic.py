# AP 01: Simple Arithmetic
# ============================================================
# Description: Take two numbers from user and perform all basic
#              arithmetic operations (sum, difference, product, quotient)
# ============================================================

# Prompt the user to input two numbers
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))

# Calculate sum of the two numbers
sum_result = num1 + num2

# Calculate difference between the two numbers
difference = num1 - num2

# Calculate product of the two numbers
product = num1 * num2

# Calculate quotient of the two numbers
quotient = num1 / num2

# Display the results
print("\n--- Results ---")
print("Sum:", sum_result)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)
