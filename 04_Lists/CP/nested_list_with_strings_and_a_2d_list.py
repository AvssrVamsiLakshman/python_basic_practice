mixed_list_2 = ["apple", "banana", ["x", "y"], ["cat", "dog"]]

print(mixed_list_2)
# Output: ['apple', 'banana', ['x', 'y'], ['cat', 'dog']]

print(mixed_list_2[0])  # Output: apple
print(mixed_list_2[1])  # Output: banana
print(mixed_list_2[2])  # Output: ['x', 'y']
