# ============================================================
# Concept: 2D Lists
# Topic: Simple 2D List with Integers
# ============================================================
# Description: A simple 2D list (matrix) containing only
#              integers. Each row is a list of numbers.
# ============================================================

two_d_list_1 = [
    [1, 2, 3],    # First row
    [4, 5, 6],    # Second row
    [7, 8, 9]     # Third row
]

print(two_d_list_1[1])  # Output: [4, 5, 6]

print(two_d_list_1[1][0])  # Output: 4
print(two_d_list_1[1][1])  # Output: 5
print(two_d_list_1[1][2])  # Output: 6
