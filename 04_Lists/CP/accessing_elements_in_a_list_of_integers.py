# ============================================================
# Concept: Negative Indexing
# Topic: Accessing Elements in a List of Integers
# ============================================================
# Description: Using negative indices to access elements
#              from the end of the list. -1 is the last element.
# ============================================================

numbers = [5, 10, 15, 20, 25]

print(numbers[-5])  # Output: 5
print(numbers[-4])  # Output: 10
