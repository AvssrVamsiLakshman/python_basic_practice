# ============================================================
# Concept: List Indexing
# Topic: Indexing with Numbers
# ============================================================
# Description: Accessing elements in a list using positive
#              indices. Index starts at 0 for first element.
# ============================================================

numbers = [10, 20, 30, 40, 50]

print(numbers[0])  # Output: 10 (First element)
print(numbers[2])  # Output: 30 (Third element)
print(numbers[4])  # Output: 50 (Fifth element)
