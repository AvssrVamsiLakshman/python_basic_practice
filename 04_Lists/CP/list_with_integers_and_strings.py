# Concept: 2D Lists
# Topic: List with Integers and Strings
# ============================================================
# Description: A nested list containing integers, string lists,
#              and number lists. Accessing with double indexing.
# ============================================================

nested_list_1 = [1, ["apple", "banana"], [3, 4, 5]]

print(nested_list_1[1])  # Output: ['apple', 'banana']

print(nested_list_1[1][0])  # Output: apple
print(nested_list_1[1][1])  # Output: banana
