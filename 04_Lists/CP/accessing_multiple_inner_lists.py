# ============================================================
# Concept: Nested Lists
# Topic: Accessing Multiple Inner Lists
# ============================================================
# Description: Accessing different inner lists from a
#              nested list using indices.
# ============================================================

nested_list = [
    ["apple", "banana", "cherry"],
    ["dog", "cat", "rabbit"],
    [10, 20, 30]
]

print(nested_list[0])
# Output: ['apple', 'banana', 'cherry']

print(nested_list[2])
# Output: [10, 20, 30]
