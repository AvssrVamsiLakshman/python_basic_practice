# ============================================================
# Concept: Nested Lists
# Topic: Mixed Data Types (Nested)
# ============================================================
# Description: A list containing mixed types including
#              another nested list.
# ============================================================

mixed_list_1 = [10, "world", 3.14, [True, False]]

print(mixed_list_1)
# Output: [10, 'world', 3.14, [True, False]]

print(mixed_list_1[0])  # Output: 10
print(mixed_list_1[1])  # Output: world
print(mixed_list_1[2])  # Output: 3.14
print(mixed_list_1[3])  # Output: [True, False]
