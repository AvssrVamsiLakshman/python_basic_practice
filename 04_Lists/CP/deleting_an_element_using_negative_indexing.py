# ============================================================
# Concept: List Methods - del
# Topic: Deleting an Element Using Negative Indexing
# ============================================================
# Description: Using del with negative indexing to remove
#              elements from the end of the list.
# ============================================================

fruits = ["apple", "banana", "cherry", "date"]

del fruits[-1]

print(fruits)  # Output: ['apple', 'banana', 'cherry']
