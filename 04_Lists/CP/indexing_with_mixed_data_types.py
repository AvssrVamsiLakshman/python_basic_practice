# ============================================================
# Concept: List Indexing
# Topic: Indexing with Mixed Data Types
# ============================================================
# Description: Lists can contain different data types.
#              Accessing elements works the same way.
# ============================================================

mixed_list = [1, "hello", 3.14, True]

print(mixed_list[0])  # Output: 1 (First element)
print(mixed_list[1])  # Output: hello (Second element)
print(mixed_list[2])  # Output: 3.14 (Third element)
print(mixed_list[3])  # Output: True (Fourth element)
