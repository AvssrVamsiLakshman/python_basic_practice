# ============================================================
# Concept: String Formatting
# Topic: Basic Positional Arguments
# ============================================================
# Description: Using positional arguments to format a string.
#              {0} refers to first argument, {1} to second.
# ============================================================

# Using positional arguments to format a string.
text = "{0} is {1} years old.".format("Alice", 30)

print(text)
# Output: Alice is 30 years old.
