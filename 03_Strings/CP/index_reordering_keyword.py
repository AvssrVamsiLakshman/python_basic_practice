# Concept: String Formatting
# Topic: Index Reordering (Keyword)
# ============================================================
# Description: Reordering keyword arguments in the format
#              string by specifying names.
# ============================================================

# Reordering keyword arguments in the format string.
text = "{city} is a beautiful city. {name} is exploring {city}.".format(name="David", city="Barcelona")

print(text)
# Output: Barcelona is a beautiful city. David is exploring Barcelona.
