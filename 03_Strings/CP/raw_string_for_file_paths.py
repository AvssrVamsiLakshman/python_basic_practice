# Topic: Raw String for File Paths
# ============================================================
# Description: Raw string used to represent a Windows file
#              path without escaping backslashes.
# ============================================================

# Raw string used to represent a Windows file path.
file_path = r"C:\Users\John\Documents\file.txt"

print(file_path)
# Output: C:\Users\John\Documents\file.txt
