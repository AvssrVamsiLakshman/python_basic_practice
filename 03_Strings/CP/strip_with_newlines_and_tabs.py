# ============================================================
# Concept: String Methods - strip()
# Topic: Strip with Newlines and Tabs
# ============================================================
# Description: strip() removes all whitespace including
#              newlines and tabs from both ends.
# ============================================================

text = "\n\t  hello world  \t\n"
stripped_text = text.strip()

print(stripped_text)
# Output: 'hello world'
